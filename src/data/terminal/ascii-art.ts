export const PROGRESS_BAR = (percent: number, width: number = 30) => {
  const filled = Math.floor((percent / 100) * width);
  const empty = width - filled;
  return `[${"█".repeat(filled)}${"░".repeat(empty)}] ${percent}%`;
};

